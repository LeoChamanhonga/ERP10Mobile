import { ModuleCompany } from './module-company';

/**
 * This interface defines an Module.
 *
 * @export
 * @interface Module
 */
export interface Module {
    /**
     *  The identifier.
     *
     * @type {string}
     * @memberof Module
     */
    id: string;

    /**
     * The name.
     *
     * @type {string}
     * @memberof Module
     */
    name: string;

    /**
     * The description.
     *
     * @type {string}
     * @memberof Module
     */
    description: string;

    companies: ModuleCompany[];
}
